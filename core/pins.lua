-------------------------------------------------------------------------------
---------------------------------- NAMESPACE ----------------------------------
-------------------------------------------------------------------------------
local _, ns = ...
local Class = ns.Class

-------------------------------------------------------------------------------
------------------------------------- PIN -------------------------------------
-------------------------------------------------------------------------------

local Pin = Class('Pin')

function Pin:Initialize(attrs)
    if attrs then for k, v in pairs(attrs) do self[k] = v end end
end

-------------------------------------------------------------------------------
----------------------------------- DUNGEON -----------------------------------
-------------------------------------------------------------------------------

local Dungeon = Class('Dungeon', Pin, {
    group = ns.groups.DUNGEON,
    passive = true
}) -- Dungeon

-------------------------------------------------------------------------------
------------------------------------ RAID -------------------------------------
-------------------------------------------------------------------------------

local Raid = Class('Raid', Pin, {
    group = ns.groups.RAID,
    passive = true
}) -- Raid

-------------------------------------------------------------------------------
------------------------------- SKYRIDING RACE --------------------------------
-------------------------------------------------------------------------------

local SkyridingRace = Class('SkyridingRace', Pin, {
    group = ns.groups.SKYRIDING_RACE,
    passive = true
}) -- Skyriding Race

-------------------------------------------------------------------------------

ns.pin = {
    Dungeon = Dungeon,
    Pin = Pin,
    Raid = Raid,
    SkyridingRace = SkyridingRace
}
