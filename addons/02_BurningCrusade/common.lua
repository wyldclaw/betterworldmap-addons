-------------------------------------------------------------------------------
---------------------------------- NAMESPACE ----------------------------------
-------------------------------------------------------------------------------
local _, ns = ...
local L = ns.locale

local Group = ns.Group

local Pin = ns.pin.Pin

-------------------------------------------------------------------------------

ns.expansion = 2

-------------------------------------------------------------------------------
----------------------------------- GROUPS ------------------------------------
-------------------------------------------------------------------------------

ns.groups.OUTLAND_CUP = Group({
    atlas = 'racing',
    label = L['outland_cup'],
    name = 'outland_cup',
    IsVisible = function()
        return ns.CalendarEventIsActive(1407)
    end
}) -- Outland Cup

-------------------------------------------------------------------------------
------------------------------------ PINS -------------------------------------
-------------------------------------------------------------------------------

local OutlandCup = Pin({
    group = ns.groups.OUTLAND_CUP
}) -- Outland Cup

ns.pin.OutlandCup = OutlandCup

-------------------------------------------------------------------------------
--------------------------------- MENU GROUPS ---------------------------------
-------------------------------------------------------------------------------

ns.menuGroups = {
    [101] = { -- Outland
        [1] = {
            groups = {
                ns.groups.OUTLAND_CUP
            }
        }
    }
}
